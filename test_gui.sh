#!/bin/bash

echo "=================================================="
echo "  Linux二级文件系统 - GUI模式测试脚本"
echo "  适用于: VMware Ubuntu / WSL (需图形环境)"
echo "=================================================="

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m'

echo ""
echo "──────────────────────────────────────────────────"
echo " 【GUI模式测试准备】"
echo "──────────────────────────────────────────────────"

echo ""
echo "1. 环境检查:"
echo "   - 检查ncurses库..."
if ldconfig -p | grep -q ncurses; then
    echo -e "     ${GREEN}✓ ncurses库已安装${NC}"
else
    echo -e "     ${YELLOW}⚠ ncurses库未安装，正在安装...${NC}"
    sudo apt-get update && sudo apt-get install -y libncurses5-dev libncursesw5-dev
fi

echo ""
echo "2. 编译项目:"
cd /home/ubuntu1/osfs
echo "   - 清理并重新编译..."
make clean > /dev/null 2>&1
make > /dev/null 2>&1
if [ $? -eq 0 ]; then
    echo -e "     ${GREEN}✓ 编译成功${NC}"
else
    echo -e "     ${RED}✗ 编译失败${NC}"
    exit 1
fi

echo ""
echo "3. 检查图形环境:"
if [ -n "$DISPLAY" ]; then
    echo -e "     ${GREEN}✓ DISPLAY环境变量已设置: $DISPLAY${NC}"
else
    echo -e "     ${YELLOW}⚠ DISPLAY未设置，请确保在图形环境下运行${NC}"
    echo "     WSL用户请运行: export DISPLAY=:0"
fi

echo ""
echo "──────────────────────────────────────────────────"
echo " 【GUI测试用例清单】"
echo "──────────────────────────────────────────────────"
echo ""
echo "┌────────────┬──────────────────────────────────────────────────────┐"
echo "│ 测试编号   │ 测试步骤                                              │"
echo "├────────────┼──────────────────────────────────────────────────────┤"
echo "│ GUI-01     │ 启动GUI: ./filesys 2                                 │"
echo "│            │ 输入用户名: root                                     │"
echo "│            │ 输入密码: root                                       │"
echo "│            │ 预期: 登录成功，显示文件列表界面                       │"
echo "├────────────┼──────────────────────────────────────────────────────┤"
echo "│ GUI-02     │ 创建文件: 在命令行输入 create test.txt               │"
echo "│            │ 预期: 文件列表显示 test.txt                           │"
echo "├────────────┼──────────────────────────────────────────────────────┤"
echo "│ GUI-03     │ 创建目录: 在命令行输入 mkdir docs                    │"
echo "│            │ 预期: 文件列表显示 docs <DIR>                        │"
echo "├────────────┼──────────────────────────────────────────────────────┤"
echo "│ GUI-04     │ 打开文件: 在命令行输入 open test.txt r               │"
echo "│            │ 预期: FD面板显示 fd=0(inode=?,mode=r)                │"
echo "├────────────┼──────────────────────────────────────────────────────┤"
echo "│ GUI-05     │ 写入文件: 在命令行输入 write 0 Hello World          │"
echo "│            │ 预期: 显示"写入 X 字节"                              │"
echo "├────────────┼──────────────────────────────────────────────────────┤"
echo "│ GUI-06     │ 读取文件: 在命令行输入 read 0 20                    │"
echo "│            │ 预期: 显示"读取 X 字节"和内容"Hello World"          │"
echo "├────────────┼──────────────────────────────────────────────────────┤"
echo "│ GUI-07     │ 关闭文件: 在命令行输入 close 0                      │"
echo "│            │ 预期: FD面板不再显示该fd                             │"
echo "├────────────┼──────────────────────────────────────────────────────┤"
echo "│ GUI-08     │ 目录切换: 在命令行输入 cd docs                       │"
echo "│            │ 预期: 路径显示变为 /docs                             │"
echo "├────────────┼──────────────────────────────────────────────────────┤"
echo "│ GUI-09     │ 回收站操作: trash-move test.txt                     │"
echo "│            │ 预期: 文件列表移除 test.txt                          │"
echo "├────────────┼──────────────────────────────────────────────────────┤"
echo "│ GUI-10     │ 查看回收站: 在命令行输入 trash                       │"
echo "│            │ 预期: 命令行显示回收站内容列表                        │"
echo "├────────────┼──────────────────────────────────────────────────────┤"
echo "│ GUI-11     │ 恢复文件: trash-restore test.txt_X                   │"
echo "│            │ 预期: 文件列表重新显示 test.txt                       │"
echo "├────────────┼──────────────────────────────────────────────────────┤"
echo "│ GUI-12     │ 查看日志: 在命令行输入 journal                       │"
echo "│            │ 预期: 命令行显示操作日志列表                          │"
echo "├────────────┼──────────────────────────────────────────────────────┤"
echo "│ GUI-13     │ 修改权限: chmod test.txt 755                        │"
echo "│            │ 预期: 文件权限变为 rwxr-xr-x                         │"
echo "├────────────┼──────────────────────────────────────────────────────┤"
echo "│ GUI-14     │ mmap操作: open test.txt r → mmap 0 100              │"
echo "│            │ 预期: 显示"映射成功: 地址"                           │"
echo "├────────────┼──────────────────────────────────────────────────────┤"
echo "│ GUI-15     │ 退出GUI: 在命令行输入 exit                           │"
echo "│            │ 预期: 程序正常退出，返回终端                          │"
echo "└────────────┴──────────────────────────────────────────────────────┘"

echo ""
echo "──────────────────────────────────────────────────"
echo " 【GUI测试启动命令】"
echo "──────────────────────────────────────────────────"
echo ""
echo "VMware Ubuntu:"
echo "  cd /home/ubuntu1/osfs"
echo "  ./filesys 2"
echo ""
echo "WSL (需配置X Server):"
echo "  export DISPLAY=:0"
echo "  cd /home/ubuntu1/osfs"
echo "  ./filesys 2"
echo ""
echo "提示: GUI界面包含4个窗口面板:"
echo "  - 文件列表窗口: 显示当前目录文件"
echo "  - FD面板: 显示已打开的文件描述符"
echo "  - 状态栏: 显示用户、磁盘使用、选中文件信息"
echo "  - 命令行: 输入命令进行操作"
